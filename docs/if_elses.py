# 기본 if elif else 구문
if True : 
    pass
elif True :
    pass
else :
    pass

#문자 비교
str_name="oh jisu"
str_name=="oh jisu"

#문자에 대한 긍정으로 질문
if str_name=="oh jisu" : 
    pass
    print("name is {}.".format(str_name))
else  :
    pass
print("End Program!")

# if문의 질문 형식(condition): 변수+부등호+기준값
#문자에 대한 부정으로 질문
#false는 if 안에 들어올 수 없음.
if str_name!="oh jisu" :  
    pass
    print("name is not {}.".format(str_name))
else  :
    pass
print("End Program!")
